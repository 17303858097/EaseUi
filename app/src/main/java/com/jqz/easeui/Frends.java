package com.jqz.easeui;

public class Frends {
    private String name;
    private String sex;
    private String s;

    public Frends(String name, String sex, String s) {
        this.name = name;
        this.sex = sex;
        this.s = s;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getSex() {
        return sex;
    }

    public void setSex(String sex) {
        this.sex = sex;
    }

    public String getS() {
        return s;
    }

    public void setS(String s) {
        this.s = s;
    }
}
